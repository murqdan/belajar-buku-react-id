// Apa itu ReactJS?

/* Terdapat tiga prinsip dasar dari pustaka ReactJS yang harus kita fahami. */
/* 1. Component Based
ReactJS menggunakan konsep komponen yaitu dengan memecah antar muka pengguna yang kompleks menjadi bagian-bagian kecil.
Setiap bagian tersebut dapat berdiri sendiri dan dapat digunakan secara berulang. */
/* 2. Declarative
ReactJS akan memudahkan kita merancang antar muka pengguna yang interaktif,
salah satunya dalam hal menampilkan & memperbaharui komponen dari aplikasi berdasarkan
kondisi data tertentu secara efisien & terstruktur. */
/* 3. Learn Once, Write Anywhere
Tidak hanya cocok untuk membangun aplikasi berbasis web, namun ReactJS juga dapat digunakan untuk membangun
aplikasi mobile native (iOS & Android) melalui pustaka ReactNative dan tentu saja dengan bantuan tools Javascript lain
maka aplikasi ReactJS dapat di-compile menjadi aplikasi berbasis desktop.
Nah, itu artinya dengan belajar ReactJS untuk aplikasi web, maka kemudian kita bisa gunakan pengetahuan tersebut
untuk diterapkan ketika membangun aplikasi berbasis mobile & desktop. Ibarat pepatah
"sekali mendayung, dua tiga pulau terlampaui". */
